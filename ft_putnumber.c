#include "allft.h"

int	**ft_putnumber(void)
{
	int	i;
	int	j;

	i = 0;
	while (i < g_n)
	{
		j = 0;
		while (j < g_n)
		{
			g_box[i][j] = g_box[i][j] + (j + 1);
			j++;
		}
		i++;
	}
	return (g_box);
}
